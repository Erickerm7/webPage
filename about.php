                    <h4>Sobre Aseguradora La Ceiba, S.A.</h4>
                    <p>A mediados del año de 1984, se reunió un grupo de industriales con una extraordinaria visión del futuro, para fundar una empresa aseguradora que prestara servicios eficientes a la sociedad guatemalteca. La sociedad fue constituida el 3 de diciembre de 1986 en escritura Pública No. 183, ante el Notario Juan Eladio Campos Moraga, y definitivamente inscrita en el Registro Mercantil de la República el 12 de marzo de 1987.</p> 
  
<p>
Mediante Acuerdo Gubernativo No. 695 de fecha 20 de agosto de 1987 del Presidente de la República, se reconoce la personalidad jurídica de Aseguradora La Ceiba, S.A. y se le concede autorización para operar en todos los ramos de seguros de Daños Generales, Reaseguros y demás actividades relacionadas. El 3 de noviembre de 1987 la Superintendencia de Bancos emite el Acuerdo 323-87 que la autoriza para iniciar operaciones. Aseguradora La Ceiba, S.A. abrió sus puertas al público el 10 de noviembre del mismo año, habiendo emitido la primera póliza en el mismo mes y año. </p>
   
<p>
1996 es un año muy importante para Aseguradora La Ceiba, ya que adelantándonos a las tendencias económicas mundiales, se cristaliza una alianza estratégica y accionaria con Aseguradora Mundial de Panamá, misma que se consolidó en enero 1997 y que en su oportunidad estuvo conformada por compañías de Centroamérica, El Caribe y Sur América.</p>
 
<p>
Adicionalmente, en este año la Aseguradora cambió de domicilio y se traslada al corazón de la zona 10, específicamente en el Edifico Atlántis, unos de los más modernos de Guatemala. En el mismo año se toma la decisión de empezar los trámites para la implementación de los Seguros de Personas. </p>
  
<p>
Durante los primeros meses de 1997, mediante resoluciones de la Superintendencia de Bancos, son aprobados nuestros planes de Seguros de Personas, y en un acto especialmente preparado para ese objetivo, se realiza la presentación de los nuevos productos en el mes de agosto de 1997. </p>
  
<p>
En 1999 la compañía se traslada a su propio edificio en una de las zonas de gran desarrollo, a corta distancia de las áreas de mayor importancia de la ciudad. A través de estos 26 años, Aseguradora La Ceiba, ha caminado dentro del mercado nacional con paso firme, y su crecimiento sostenido ha fortalecido su estructura financiera.</p>



