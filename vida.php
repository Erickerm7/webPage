               <h3><font color="#007d32"><strong>SEGURO COLECTIVO</strong></font></h3>

    <h4><font color="#71bf3e">SEGURO COLECTIVO DE VIDA</font></h4>
    
    <p>Orientado a empresas, asociaciones o colectividades, para brindar la protecci&oacute;n del seguro de vida a todos sus miembros, mediante una p&oacute;liza maestra a la cual se adhieren todos sus integrantes.
    </p>
    
    <h4><font color="#71bf3e">- SEGURO COLECTIVO DE VIDA PARA CUBRIR SALDOS DEUDORES</font></h4>
    
    <p>Cubre los saldos de las obligaciones adquiridas por una persona, exigibles a su fallecimiento, y es contratado por la entidad acreedora, cuyo inter&eacute;s asegurable es el importante del saldo.
    </p>
         

    
	<h3><font color="#007d32"><strong>SEGURO DE ACCIDENTES PERSONALES</strong></font></h3>		

    
    <p>Esta p&oacute;liza cubre al asegurado a un costo sumamente bajo, cuando por causas accidentales muere, sufre de invalidez, incapacidad y/o incurre en gastos m&eacute;dicos. Se ofrece a trav&eacute;s de las siguientes modalidades:
    </p>
    
    <h4><font color="#71bf3e"> SEGURO CONTRA ACCIDENTES PERSONALES &quot;INDIVIDUAL Y FAMILIAR&quot;</font></h4>
    
    <p>Se puede contratar en forma individual o para asegurar a un n&uacute;cleo familiar (c&oacute;nyuges e hijos dependientes menores de 23 a&ntilde;os).
    </p>
    
    <h4><font color="#71bf3e">- SEGURO CONTRA ACCIDENTES PERSONALES &quot;COLECTIVO&quot;</font></h4>
    
    <p>Orientado a empresas, asociaciones o colectividades, para brindar la protecci&oacute;n del seguro a todos sus miembros, emitiendo una sola p&oacute;liza maestra.
    </p>
    
    <h4><font color="#71bf3e">- SEGURO CONTRA ACCIDENTES PERSONALES &quot;ESCOLAR&quot;</font></h4>
    
    <p>Espec&iacute;ficamente cubre a los alumnos de establecimientos educativos. Cubre gastos m&eacute;dicos ocasionados por un accidente, ya sea dentro o fuera del establecimiento educativo. <a href="download/tarifas_escolares_2013.pdf" target="_blank"><strong>Ver Tarifas</strong></a>

    </p>
      
	<h3><font color="#007d32"><strong>SEGURO DE VIDA INDIVIDUAL</strong></font></h4>	            
	<p>Cubre cualquier p&eacute;rdida por causa externa, exceptuando las exclusiones que se detallan en las condiciones generales de la p&oacute;liza.             
    </p>
    
    <h3><font color="#007d32"><strong>RIESGOS ORDINARIOS DE TR&Aacute;NSITO</strong></font></h4>	            

    
    <h4><font color="#71bf3e">- PLAN TEMPORAL RENOVABLE (N) A&Ntilde;OS:</font></h4>
    
    <p>Alta cobertura contra el riesgo de muerte, no importando su causa, por medio de pagos peri&oacute;dicos considerablemente econ&oacute;micos, y garantizando a cada a&ntilde;o la renovaci&oacute;n del mismo. Se conceden descuentos en las primas, luego de que el asegurado mantenga en vigor su seguro por lo menos durante 5 a&ntilde;os. Contempla la oportunidad de convertirlo a un programa que forme ahorro, y as&iacute; tener un capital que garantice un fondo de jubilaci&oacute;n, una renta vitalicia, o bien para cubrir cualquier otra circunstancia del asegurado.
    </p>
    
    <h4><font color="#71bf3e">- SEGURO DE VIDA CON AHORRO , PLAN VITAL:</font></h4>
    
    <p>Adem&aacute;s de cubrir la muerte del asegurado, este seguro contempla la creaci&oacute;n de un fondo de reserva para garantizar un fondo de jubilaci&oacute;n, una renta vitalicia, o para cubrir cualquier circunstancia del asegurado.
    </p>
               
               
               
               
               
               
               
               
               
               
                    
                        
